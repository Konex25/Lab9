public abstract class Person {
    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Getter for name
    public String getName() {
        return name;
    }

    // Getter for age
    public int getAge() {
        return age;
    }

    // Simple intro
    public void introduce() {
        System.out.println("Name: " + name + " | Age: " + age + " years old.");
    }
}